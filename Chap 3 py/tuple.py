# Tuples In Python:
# A tuple is a collection which is ordered and immutable(unchangeable). In Python tuples are written with round brackets.

# Creating a Tuple:]
# To create a tuple in Python, place all the elements in a round bracket, separated by commas.
# Eg:
tuple=(1,2,3,4,3,7,8,4,3,5,2,3,5,7,2)
#checking the type of tuple:
print(type(tuple))
# Accessing the elements of tuple:
# we can access the elements of tuple by referring to the index number, inside square brackets.
print(tuple[1 ])
# Tuple Methods:
# Tuple methods are used to perform operations on tuple.
print(tuple.count(3)) #it will return the number of times 3 repeats in the tuple.
print(tuple.index(5)) #it will return the index of the element .
